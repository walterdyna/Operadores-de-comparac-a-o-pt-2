/*
Operadores de comparação pt.2

   >  ---> Maior
   <  ---> Menor

   >= ---> Maior ou Igual
   <= ---> Menor ou Igual
   */

//--------------------> Comparação Maior e Menor <--------------------
const maiorUM = 20
const maiorDOIS = 30

console.log(maiorUM > maiorDOIS)

const menorUM = 10
const menorDOIS = 20

console.log(menorUM < menorDOIS)

//-----------------> Comparação Maior, Menor e igual <-----------------
const maiorIgualUM = 33
const maiorIgualDOIS = 43

console.log(maiorIgualUM >= maiorIgualDOIS)

const menorIgualUM = 43
const menorIgualDOIS = 53

console.log(menorIgualUM <= menorIgualDOIS)